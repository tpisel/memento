func TestRenderProducesDeterministicMarkdownProjection(t *testing.T) {
	m := manifest.Manifest{
		Entries: []manifest.Entry{
			{
				Key:     "Architecture decision record/adr-0002.md",
				Title:   "ADR 0002",
				Summary: "Second ADR.",
				Bytes:   222,
				Lines:   12,
				Tags:    []string{"brief"},
				Mode:    markdown.ModeAppendOnly,
			},
			{
				Key:     "notes/beta.md",
				Title:   "Beta",
				Summary: "A compact note.",
				Bytes:   1234,
				Lines:   7,
				Tags:    []string{"brief"},
				Mode:    markdown.ModeAppendOnly,
			},
			{
				Key:     "Architecture decision record/adr-0001.md",
				Title:   "ADR 0001",
				Summary: "First ADR.",
				Bytes:   111,
				Lines:   11,
				Tags:    []string{"memento"},
				Mode:    markdown.ModeAppendOnly,
			},
			{
				Key:     "zeta.md",
				Title:   "Alpha <Beta>",
				Summary: "Summary with <angle> & ampersand.",
				Bytes:   342,
				Lines:   32,
				Tags:    []string{"memento", "brief"},
				Mode:    markdown.ModeReadOnly,
				Headings: []manifest.Heading{
					{Level: 2, Text: "Context", Slug: "context"},
					{Level: 3, Text: "Decision & Tradeoffs", Slug: "decision-tradeoffs"},
				},
				SummaryStale: true,
				Links: manifest.Links{
					Out: []manifest.OutLink{{Target: "other.md", Type: "wiki", Resolved: true}},
				},
			},
			{
				Key:     "notes/gamma.md",
				Title:   "Gamma",
				Summary: "A larger note.",
				Bytes:   12345,
				Lines:   250,
				Tags:    []string{"memento"},
				Mode:    markdown.ModeAppendOnly,
			},
			{
				Key:     "empty.md",
				Title:   "Zeta",
				Summary: "",
				Tags:    []string{},
				Mode:    markdown.ModeAppendOnly,
			},
		},
		Tags: map[string]int{
			"brief":   2,
			"memento": 2,
		},
	}

	first := Render(m)
	second := Render(m)
	if !bytes.Equal(first, second) {
		t.Fatalf("Render output changed between runs:\nfirst:\n%s\nsecond:\n%s", first, second)
	}

	want := `<!-- manifest: sha256:c41525d503ce363f7fa6183e559a9f53fbd0d621ddf264a0a533d2fe49a3e869 -->
---
mode: read-only
---
> [!caution] Auto-generated by ` + "`memento compile`" + `
> Any edits to this file will be overwritten on the next compile run.

# Memento Brief

## (root)

### 1. Zeta

key: ` + "`empty.md`" + ` | mode: ` + "`append-only`" + ` | tags: none | size: 0B / 0 lines

Summary: none

Headings: none

### 2. Alpha <Beta>

key: ` + "`zeta.md`" + ` | mode: ` + "`read-only`" + ` | tags: ` + "`brief`" + `, ` + "`memento`" + ` | size: 342B / 32 lines

Summary with <angle> & ampersand.

Headings: Context; Decision & Tradeoffs

## Architecture decision record

### 3. ADR 0001

key: ` + "`Architecture decision record/adr-0001.md`" + ` | mode: ` + "`append-only`" + ` | tags: ` + "`memento`" + ` | size: 111B / 11 lines

First ADR.

Headings: none

### 4. ADR 0002

key: ` + "`Architecture decision record/adr-0002.md`" + ` | mode: ` + "`append-only`" + ` | tags: ` + "`brief`" + ` | size: 222B / 12 lines

Second ADR.

Headings: none

## notes

### 5. Beta

key: ` + "`notes/beta.md`" + ` | mode: ` + "`append-only`" + ` | tags: ` + "`brief`" + ` | size: 1.2k / 7 lines

A compact note.

Headings: none

### 6. Gamma

key: ` + "`notes/gamma.md`" + ` | mode: ` + "`append-only`" + ` | tags: ` + "`memento`" + ` | size: 12k / 250 lines

A larger note.

Headings: none

---

Tag frequency: brief=2, memento=2
Tool files: none
`
	if string(first) != want {
		t.Fatalf("Render() =\n%s\nwant:\n%s", first, want)
	}

	if !bytes.Contains(first, []byte("> [!caution]")) {
		t.Fatalf("Render() missing Obsidian callout marker:\n%s", first)
	}

	for _, forbidden := range []string{"<!-- Auto-generated", "summary_stale", "links", "slug", "\\u003c", "\\u003e", "\\u0026"} {
		if bytes.Contains(first, []byte(forbidden)) {
			t.Fatalf("Render() contains forbidden %q:\n%s", forbidden, first)
		}
	}
}

func TestNumberedEntriesArePureAndDeterministic(t *testing.T) {
	m := manifest.Manifest{
		Entries: []manifest.Entry{
			{Key: "b/two.md", Title: "Two"},
			{Key: "a/b/three.md", Title: "Three"},
			{Key: "root.md", Title: "Root"},
			{Key: "a/one.md", Title: "One"},
		},
	}

	first := NumberedEntries(m)
	second := NumberedEntries(m)

	want := []struct {
		number int
		folder string
		key    string
	}{
		{1, "(root)", "root.md"},
		{2, "a", "a/one.md"},
		{3, "a/b", "a/b/three.md"},
		{4, "b", "b/two.md"},
	}
	if len(first) != len(want) {
		t.Fatalf("NumberedEntries() len = %d, want %d: %#v", len(first), len(want), first)
	}
	if len(second) != len(want) {
		t.Fatalf("NumberedEntries() second len = %d, want %d: %#v", len(second), len(want), second)
	}
	for i := range want {
		if first[i].Number != want[i].number || first[i].Folder != want[i].folder || first[i].Entry.Key != want[i].key {
			t.Fatalf("NumberedEntries()[%d] = %#v, want number=%d folder=%q key=%q", i, first[i], want[i].number, want[i].folder, want[i].key)
		}
		if !reflect.DeepEqual(second[i], first[i]) {
			t.Fatalf("NumberedEntries() changed between runs:\nfirst=%#v\nsecond=%#v", first, second)
		}
	}
}

func TestRenderSuffixesResolvedSummaryWikiLinksWithEntryNumbers(t *testing.T) {
	m := manifest.Manifest{
		Entries: []manifest.Entry{
			{
				Key:   "alpha.md",
				Title: "Alpha",
				Summary: "Resolved [[beta]], display [[beta|Beta note]], broken [[missing]], " +
					"and anchored [[beta#Decision]] links.",
				Mode: markdown.ModeAppendOnly,
			},
			{
				Key:     "beta.md",
				Title:   "Beta",
				Summary: "Target.",
				Mode:    markdown.ModeAppendOnly,
			},
		},
	}

	got := string(Render(m))
	for _, want := range []string{
		"Resolved [[beta @ 2]], display [[beta|Beta note @ 2]], broken [[missing]], and anchored [[beta#Decision]] links.",
		"Target.",
	} {
		if !bytes.Contains([]byte(got), []byte(want)) {
			t.Fatalf("Render() =\n%s\nwant %q", got, want)
		}
	}
}

func TestRenderWithToolFilesListsDetectedFiles(t *testing.T) {
	got := string(RenderWithToolFiles(manifest.Manifest{}, []string{"_memento/review.md", "_memento/writing.md"}))

	if !bytes.Contains([]byte(got), []byte("Tool files: _memento/review.md, _memento/writing.md\n")) {
		t.Fatalf("RenderWithToolFiles() =\n%s\nwant tool file footer", got)
	}
}

func TestFriendlyBytes(t *testing.T) {
	tests := map[int64]string{
		0:       "0B",
		342:     "342B",
		1234:    "1.2k",
		12345:   "12k",
		1400000: "1.4M",
	}

	for bytes, want := range tests {
		if got := friendlyBytes(bytes); got != want {
			t.Fatalf("friendlyBytes(%d) = %q, want %q", bytes, got, want)
		}
	}
}

func TestDetectToolFilesHandlesMissingNamespace(t *testing.T) {
	files, err := DetectToolFiles(vault.Vault{Root: t.TempDir()})
	if err != nil {
		t.Fatalf("DetectToolFiles() error = %v, want nil", err)
	}
	if len(files) != 0 {
		t.Fatalf("DetectToolFiles() = %#v, want empty", files)
	}
}

func TestDetectToolFilesListsKnownFiles(t *testing.T) {
	root := t.TempDir()
	dir := filepath.Join(root, vault.ToolDirName)
	if err := os.MkdirAll(dir, 0o755); err != nil {
		t.Fatalf("mkdir tool dir: %v", err)
	}
	for _, name := range []string{"writing.md", "review.md", "notes.md"} {
		if err := os.WriteFile(filepath.Join(dir, name), []byte("# "+name+"\n"), 0o644); err != nil {
			t.Fatalf("write %s: %v", name, err)
		}
	}

	files, err := DetectToolFiles(vault.Vault{Root: root})
	if err != nil {
		t.Fatalf("DetectToolFiles() error = %v, want nil", err)
	}
	want := []string{"_memento/review.md", "_memento/writing.md"}
	if len(files) != len(want) {
		t.Fatalf("DetectToolFiles() = %#v, want %#v", files, want)
	}
	for i := range want {
		if files[i] != want[i] {
			t.Fatalf("DetectToolFiles() = %#v, want %#v", files, want)
		}
	}
}
